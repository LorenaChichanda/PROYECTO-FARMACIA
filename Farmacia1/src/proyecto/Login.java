    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigo = (this.jTextField2.getText());
        String marca = (this.jPasswordField1.getText());
        try {
            cst = con.prepareCall("select usuario,contraseña from login where usuario=? and contraseña=?");
            cst.setString(1, codigo);
            cst.setString(2, marca);
            r = cst.executeQuery();

            if (r.next()) {

                Ventana1 jFrame = new Ventana1();//"Frame2" es el nombre que tu le pusiste a tu 2do jframe
                jFrame.setVisible(true);
             //   taxi_amigo.jLabel37.setText(jTextField2.getText());
              dispose();

            } else {
                jLabel2.setForeground(Color.red);
                jLabel2.setText("USUARIO O CONTRASEÑA INCORRECTO");

            }

        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.toString());
              JOptionPane.showMessageDialog(this, "Cliente No Encontrado", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

