     public void mostrarDatos(String valor){
         double tot=0;
         String titulo[]={"Cantidad","Descripcion","Precio Unitario"};
     String sql ="";
     sql="select * from factura where cedula='"+ valor + "'";
     String registro[]=new String[8];
     modelo=new DefaultTableModel(null,titulo);
     try{
           Statement st = cn.createStatement();
           ResultSet rs= st.executeQuery(sql);
           while(rs.next()){
               txtcedula.setText(rs.getString(1));
              txtnombre.setText(rs.getString(2));
              txtfecha.setText(rs.getString(6));
              cbproducto.setSelectedItem(rs.getString(3));
              //txtprecio.setText(rs.getString(8));
              //txtcantidad.setText(rs.getString(4));
              txttotal.setText(rs.getString(5));
              registro[0]=rs.getString(4);
              registro[1]=rs.getString(3);
              registro[2]=rs.getString(8);
              modelo.addRow(registro);
              tot+=rs.getDouble(8)*rs.getInt(4);
              txtnumfactura.setText(rs.getString(7));
               }
           table.setModel(modelo);
           txttotal.setText(String.valueOf(tot));
        }catch(SQLException ex){
           System.out.println(ex.getMessage());
        }
    }

